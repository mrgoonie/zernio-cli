import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import YAML from 'yaml';

const inputPath = resolve('docs/openapi/zernio-api-openapi.yaml');
const outputPath = resolve('src/generated/openapi-catalog.ts');

const spec = YAML.parse(readFileSync(inputPath, 'utf8'));
const methods = new Set(['get', 'post', 'put', 'patch', 'delete', 'head', 'options']);

function oneLine(value, max = 500) {
  if (!value) return undefined;
  const text = String(value).replace(/\s+/g, ' ').trim();
  return text.length > max ? `${text.slice(0, max - 1)}...` : text;
}

function schemaInfo(schema) {
  if (!schema) return {};
  if (schema.$ref) return { schemaRef: schema.$ref.split('/').pop() };
  return {
    schemaType: schema.type,
    schemaEnum: Array.isArray(schema.enum) ? schema.enum : undefined,
  };
}

function resolveRef(value) {
  if (!value?.$ref) return value;
  const parts = value.$ref.replace(/^#\//, '').split('/');
  return parts.reduce((current, part) => current?.[part], spec) || value;
}

function collectParameters(pathItem, operation) {
  return [...(pathItem.parameters || []), ...(operation.parameters || [])].map((rawParam) => {
    const param = resolveRef(rawParam);
    return {
      name: param.name || rawParam.$ref?.split('/').pop(),
      in: param.in || 'unknown',
      required: Boolean(param.required),
      description: oneLine(param.description, 240),
      ...schemaInfo(resolveRef(param.schema)),
    };
  });
}

function requestBodyInfo(rawRequestBody) {
  if (!rawRequestBody) return undefined;
  const requestBody = resolveRef(rawRequestBody);
  const content = requestBody.content || {};
  return {
    required: Boolean(requestBody.required),
    description: oneLine(requestBody.description, 240),
    contentTypes: Object.keys(content),
    schemaRefs: Object.fromEntries(
      Object.entries(content).map(([type, value]) => [type, schemaInfo(value.schema)]),
    ),
  };
}

const endpoints = [];

for (const [path, pathItem] of Object.entries(spec.paths || {})) {
  for (const [method, operation] of Object.entries(pathItem)) {
    if (!methods.has(method)) continue;
    const fallbackId = `${method}_${path}`.replace(/[^a-zA-Z0-9]+/g, '_').replace(/^_|_$/g, '');
    endpoints.push({
      operationId: operation.operationId || fallbackId,
      method: method.toUpperCase(),
      path,
      tags: operation.tags || [],
      summary: oneLine(operation.summary, 180),
      description: oneLine(operation.description),
      parameters: collectParameters(pathItem, operation),
      requestBody: requestBodyInfo(operation.requestBody),
    });
  }
}

endpoints.sort((a, b) => `${a.tags[0] || ''}:${a.path}:${a.method}`.localeCompare(`${b.tags[0] || ''}:${b.path}:${b.method}`));

const info = {
  title: spec.info?.title || 'Zernio API',
  version: spec.info?.version || 'unknown',
  servers: spec.servers || [],
  operationCount: endpoints.length,
};

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  [
    '/* This file is generated by scripts/generate-openapi-catalog.mjs. Do not edit manually. */',
    `export const openApiInfo = ${JSON.stringify(info)} as const;`,
    `export const openApiEndpoints = ${JSON.stringify(endpoints)} as const;`,
    'export type OpenApiEndpoint = (typeof openApiEndpoints)[number];',
    '',
  ].join('\n'),
);

console.log(`Generated ${endpoints.length} OpenAPI endpoints at ${outputPath}`);
